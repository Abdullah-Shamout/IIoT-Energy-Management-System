@fmcp.tool(
    name="set_motor_intensity",
    description="Set the DC motor intensity as a percentage (0–100). "
                "100% corresponds to approximately 350 W."
)
def _tool_set_motor_intensity(percent_0_to_100: float) -> dict:
    """
    Set motor intensity.

    Args:
        percent_0_to_100: Motor intensity percentage between 0 and 100.
    """
    if percent_0_to_100 is None or not (0 <= percent_0_to_100 <= 100):
        return {"error": "Invalid intensity value. Must be between 0 and 100"}
    db.update_device_intensity(config.MOTOR_ID, str(percent_0_to_100))
    power = (percent_0_to_100 / 100.0) * 350
    return {
        "success": True,
        "message": f"Motor intensity set to {percent_0_to_100}%",
        "power_w": power
    }


@fmcp.tool(
    name="db_read",
    description=(
        "Query the energy management database. "
        "query_type options: "
        "'latest_device_status' — returns all devices and their current status; "
        "'current_total_consumption' — returns total energy consumed in Wh and kWh; "
        "'device_consumption' — returns consumption for a specific device (requires device_id); "
        "'budget' — returns the configured energy budget in kWh and Wh; "
        "'historical_consumption' — returns time-series readings for a device (requires device_id, optional hours)."
    )
)
def _tool_db_read(
    query_type: str,
    device_id: int = None,
    hours: int = 24
) -> dict:
    """
    Query the database.

    Args:
        query_type: Type of query. One of: latest_device_status, current_total_consumption,
                    device_consumption, budget, historical_consumption.
        device_id:  Device ID (required for device_consumption and historical_consumption).
        hours:      Number of hours of history to retrieve (default 24).
    """
    if query_type == "latest_device_status":
        devices = db.get_all_devices()
        return {"devices": [dict(d) for d in devices]}

    elif query_type == "current_total_consumption":
        system_config = db.get_system_config()
        return {
            "total_consumption_wh":  system_config['total_consumption'],
            "total_consumption_kwh": system_config['total_consumption'] / 1000
        }

    elif query_type == "device_consumption":
        device = db.get_device(device_id)
        if device:
            return {
                "device_id":       device['id'],
                "device_name":     device['device_name'],
                "consumption_wh":  device['device_total_consumption'],
                "consumption_kwh": device['device_total_consumption'] / 1000
            }
        return {"error": "Device not found"}

    elif query_type == "budget":
        system_config = db.get_system_config()
        return {
            "energy_budget_kwh": system_config['energy_budget'],
            "energy_budget_wh":  system_config['energy_budget'] * 1000
        }

    elif query_type == "historical_consumption":
        readings = db.get_recent_readings(device_id, hours)
        return {"readings": [dict(r) for r in readings]}

    else:
        return {"error": f"Unknown query type: {query_type}"}


@fmcp.tool(
    name="read_system_status",
    description="Get the complete system status: all device states, total consumption, "
                "energy budget, and whether the budget has been exceeded."
)
def _tool_read_system_status() -> dict:
    """Get complete system status."""
    system_config = db.get_system_config()
    devices = db.get_all_devices()

    total_consumption = system_config['total_consumption']
    energy_budget     = system_config['energy_budget'] * 1000  # convert kWh → Wh

    status = "Normal" if total_consumption <= energy_budget else "Exceeded"

    return {
        "system_status":        status,
        "total_consumption_wh": total_consumption,
        "energy_budget_wh":     energy_budget,
        "devices":              [dict(d) for d in devices],
        "last_update":          _now_kuwait()   # Kuwait time
    }


