@fmcp.tool(
    name="generate_energy_report",
    description="Generate an energy consumption report for a given time period. "
                "period: 'today', 'week', or 'month'. "
                "report_type: 'summary' or 'detailed'."
)
def _tool_generate_energy_report(
    period: str = "today",
    report_type: str = "summary"
) -> dict:
    """
    Generate an energy report.

    Args:
        period:      Reporting period. One of: 'today', 'week', 'month'.
        report_type: Report detail level. One of: 'summary', 'detailed'.
    """
    devices = db.get_all_devices()
    system_config = db.get_system_config()

    report = {
        "period":               period,
        "report_type":          report_type,
        "generated_at":         _now_kuwait(),   # Kuwait time
        "total_consumption_kwh": system_config['total_consumption'] / 1000,
        "energy_budget_kwh":    system_config['energy_budget'],
        "devices":              []
    }

    for device in devices:
        report["devices"].append({
            "name":            device['device_name'],
            "status":          device['device_status'],
            "consumption_kwh": device['device_total_consumption'] / 1000,
            "percentage":      (
                device['device_total_consumption'] / system_config['total_consumption'] * 100
                if system_config['total_consumption'] > 0 else 0
            )
        })

    return report


@fmcp.tool(
    name="calculate_energy_metrics",
    description="Calculate derived energy metrics from raw electrical measurements."
)
def _tool_calculate_energy_metrics(
    power_w:        float = 0,
    voltage_v:      float = 220,
    current_a:      float = 0,
    runtime_hours:  float = 1
) -> dict:
    """
    Calculate energy metrics.

    Args:
        power_w:       Active power in Watts.
        voltage_v:     Voltage in Volts (default 220 V).
        current_a:     Current in Amperes.
        runtime_hours: Operating duration in hours (default 1 h).
    """
    energy_kwh         = (power_w * runtime_hours) / 1000
    apparent_power_va  = voltage_v * current_a
    power_factor       = power_w / apparent_power_va if apparent_power_va > 0 else 0
    reactive_power_var = (
        (apparent_power_va ** 2 - power_w ** 2) ** 0.5
        if apparent_power_va > power_w else 0
    )
    co2_emissions_kg   = energy_kwh * 0.5

    return {
        "energy_kwh":          energy_kwh,
        "apparent_power_va":   apparent_power_va,
        "power_factor":        power_factor,
        "reactive_power_var":  reactive_power_var,
        "co2_emissions_kg":    co2_emissions_kg,
    }


# ── MCPServer wrapper (used by app.py) ────────────────────────────────────────

class MCPServer:
    """
    Thin wrapper around the FastMCP tool functions.
    Provides the same interface that app.py expects:
      - call_tool(tool_name, parameters)
      - turn_on_device(params)
      - turn_off_device(params)
      - set_motor_intensity(params)
    """

    def __init__(self):
        self.tools = {
            "turn_on":                       self.turn_on_device,
            "turn_off":                      self.turn_off_device,
            "set_motor_intensity":           self.set_motor_intensity,
            "db_read":                       self.db_read,
            "read_system_status":            self.read_system_status,
            "request_human_approval":        self.request_human_approval,
            "generate_optimization_schedules": self.generate_optimization_schedules,
            "generate_energy_report":        self.generate_energy_report,
            "calculate_energy_metrics":      self.calculate_energy_metrics,
        }
        print("✓ MCP Server initialized with 9 registered tools")

    def call_tool(self, tool_name: str, parameters: dict) -> dict:
        if tool_name in self.tools:
            return self.tools[tool_name](parameters)
        return {"error": f"Unknown tool: {tool_name}"}

    # ── Device control ────────────────────────────────────────────────────────

    def turn_on_device(self, params: dict) -> dict:
        return _tool_turn_on(params.get("device"))

    def turn_off_device(self, params: dict) -> dict:
        return _tool_turn_off(params.get("device"))

    def set_motor_intensity(self, params: dict) -> dict:
        return _tool_set_motor_intensity(params.get("percent_0_to_100"))

    # ── Data / query tools ────────────────────────────────────────────────────

    def db_read(self, params: dict) -> dict:
        return _tool_db_read(
            query_type=params.get("query_type"),
            device_id=params.get("device_id"),
            hours=params.get("hours", 24)
        )

    def read_system_status(self, params: dict) -> dict:
        return _tool_read_system_status()

    def request_human_approval(self, params: dict) -> dict:
        return _tool_request_human_approval(params.get("plan", ""))

    def generate_optimization_schedules(self, params: dict) -> dict:
        return _tool_generate_optimization_schedules(
            params.get("budget_exceeded_by_wh", 0)
        )

    def generate_energy_report(self, params: dict) -> dict:
        return _tool_generate_energy_report(
            period=params.get("period", "today"),
            report_type=params.get("report_type", "summary")
        )

    def calculate_energy_metrics(self, params: dict) -> dict:
        return _tool_calculate_energy_metrics(
            power_w=params.get("power_w", 0),
            voltage_v=params.get("voltage_v", 220),
            current_a=params.get("current_a", 0),
            runtime_hours=params.get("runtime_hours", 1)
        )


mcp_server = MCPServer()
